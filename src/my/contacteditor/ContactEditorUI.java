    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     int selectedRow = jTable1.getSelectedRow(); // Получаем индекс выделенной строки
     

    if (selectedRow == -1) { // Если ничего не выбрано
        JOptionPane.showMessageDialog(this, "Выберите строку для вычисления!", "Ошибка", JOptionPane.ERROR_MESSAGE);
        return;
    }

    
    
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    // Проверяем, вычислена ли уже строка
    Object resultValue = model.getValueAt(selectedRow, 3);
    if (!"-".equals(resultValue.toString())) {
        JOptionPane.showMessageDialog(this, "Эта строка уже вычислена!", "Ошибка", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Получаем значения
    double a = (double) model.getValueAt(selectedRow, 0);
    double b = (double) model.getValueAt(selectedRow, 1);
    double dx = (double) model.getValueAt(selectedRow, 2);

    // Вычисляем интеграл
    double result = trapezoidalIntegral(a, b, dx);

    // Записываем результат в таблицу
    model.setValueAt(result, selectedRow, 3);
    }//GEN-LAST:event_jButton3ActionPerformed



    
    
