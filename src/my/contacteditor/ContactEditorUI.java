    private double trapezoidalIntegral(double a, double b, double dx) {
        
     
     int n = (int) Math.ceil((b - a) / dx); // Вычисляем количество разбиений по переданному шагу
    double s1 = 0, s2;

    do {
        s2 = 0;
        for (int i = 1; i < n; i++) {
            double x = a + i * dx;
            s2 += function(x);
        }
        s2 = (s2 + function(a) / 2 + function(b) / 2) * dx;

        double temp = s1;
        s1 = s2;
        s2 = temp;

        // Теперь `dx` не меняем!
    } while (Math.abs(s1 - s2) > 1e-2); // Возможно, здесь ты имел в виду какую-то точность?

    return s1;
    }

private double function(double x) {
    return Math.exp(x) / x;
}

    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          try {
        double a = Double.parseDouble(jTextField1.getText());
        double b = Double.parseDouble(jTextField2.getText());
        double step = Double.parseDouble(jTextField3.getText());
  // Проверки на корректность
        if (step <= 0) {
            JOptionPane.showMessageDialog(this, "Шаг должен быть больше 0!", "Ошибка", JOptionPane.ERROR_MESSAGE);
            return;  // Прерываем выполнение
        }
        if (a >= b) {
            JOptionPane.showMessageDialog(this, "Начальное значение a должно быть меньше b!", "Ошибка", JOptionPane.ERROR_MESSAGE);
            return;  // Прерываем выполнение
        }
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        // Проверяем, есть ли уже такая строка в таблице
        for (int i = 0; i < model.getRowCount(); i++) {
            double rowA = (double) model.getValueAt(i, 0);
            double rowB = (double) model.getValueAt(i, 1);
            double rowStep = (double) model.getValueAt(i, 2);

            if (rowA == a && rowB == b && rowStep == step) {
                JOptionPane.showMessageDialog(this, "Такие данные уже добавлены!", "Ошибка", JOptionPane.ERROR_MESSAGE);
                return; // Выход из метода, если запись уже существует
            }
        }

        // Если дубликатов нет, добавляем новую строку
        model.addRow(new Object[]{a, b, step, "-"});
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ошибка ввода! Введите числа.", "Ошибка", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

